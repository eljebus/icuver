			<p style='margin-top:0'>Los sistemas de producción, manipulación y distribución de alimentos en los países en desarrollo cobran cada vez mayor importancia por el impacto en el rendimiento laboral de una ingesta de alimentos de baja calidad en presentación o preparados en condiciones poco higiénicas debido a una carencia de cultura  en calidad higiénica y de servicio, de quienes ejercen la importante función de alimentar a los trabajadores en las organizaciones y empresas.</p>

			<p>Esto da a lugar a problemas de calidad e higiene en la producción de alimentos, como lo es la generación de bacterias, tales como la  salmonella, E-collí, Cisticercos, entre otras no menos dañinas, que afectan la salud, disposición y eficiencia del sector laboral en todos sus niveles, por ello la necesidad de ajustarse a las normas internacionalmente aceptadas en materia de calidad e inocuidad alimentaria así como la falta de seguimiento en dichos procedimientos lo cual asegure una excelente producción debidamente adecuada y que permita tener un buen control en el reporte de fallas o quejas que demeriten la confiabilidad de los alimentos y servicios dentro de la industria.</p>

			<p>Con base en este escenario el Instituto Culinario de Veracruz, se permite presentar a ustedes el proyecto ISC Implementación de Sistemas de Calidad, para mejorar la calidad de la atención alimentaria de los centros de trabajo, comedores industriales, restaurantes y cualquier tipo de proyecto de índole  alimentaria, con el objeto de asegurar la calidad e inocuidad de los alimentos y en general la atención de los colaboradores directos e indirectos de la empresa u organización.</p>

			<p>Para ello hemos implementado un programa de capacitación modular e implementación de procedimientos  que permite a quien los curse, acreditar materias vinculadas con el manejo adecuado de alimentos, manejo del servicio de comedor, actividades de hospedaje, así como de administración, liderazgo y calidad; para lograr alcanzar altos estándares de calidad en el sector.</p>

			<p class="title">Objetivo General</p>

			<p>Establecer un proceso de control efectivo de la calidad e inocuidad alimentaria mediante un programa teórico-práctico que permita mantener altos estándares de servicio de alimentación y hospedaje de clase mundial.</p>

			<p class="title">Objetivos Específicos</p>
			<ul>
				<li>Ofrecer un plan de ISC IMPLEMENTACION de SISTEMA de CALIDAD enfocado al requerimiento de servicio especializado mediante un diagnostico en un adecuado manejo higiénico en general, respaldado por la normas de inocuidad, verificado mediante evaluaciones de campo en el área que se esté ejerciendo ya sea gastronómica y /o de hospedaje.</li>

				<li>Proporcionar capacitación que permita a los prestadores de servicios adquirir las habilidades y buenas prácticas para el manejo adecuado de los alimentos y bebidas, así como, para la administración y manejo de hospedaje, de unidades remotas de exploración, perforación y flóteles.
				</li>

				<li>Acreditar las competencias adquiridas para su aplicación en el área laboral.</li>

				<li>Evaluar periódicamente la correcta aplicación de los procesos mediante un plan de calidad y mejora continua basado en la actualización de las normas y competencias laborales.</li>

				<li>Acreditar los procedimientos que estandaricen los servicios de alimentación y hospedaje de clase mundial.
				</li>

			</ul>

